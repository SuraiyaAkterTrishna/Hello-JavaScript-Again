// variable declaration
var price = 99;
// console.log(price);

// variable value update
price = 199;
// console.log(price);

// add something to the previous value
var price1 = 60;
var price2 = 80;

// set a new value
price1 = 90;
// console.log(price1);

// add some value to the existing value
price1 = price1 + 100;
// console.log(price1);

// addition to the same variable shorthand
// += means adding some value to the existing value
price1 += 20;
// console.log(price1);

price2 = price2 - 10;
// console.log(price2)
// shorthand -=
// -= means deducting some values from the current value of the variable
price2 -= 20;
// console.log(price2)

// *= 
price1 *= 10;

// /=
price1 /= 5;

